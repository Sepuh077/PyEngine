def create_project(project_name: str, target_dir: Optional[Path] = None) -> Path:
    """
    Create a new Engine3D project with the given name.

    Args:
        project_name: Name of the project
        target_dir: Directory where project will be created (default: current directory)

    Returns:
        Path to the created project directory
    """
    # Determine target directory
    if target_dir is None:
        target_dir = Path.cwd()
    else:
        target_dir = Path(target_dir)

    # Create project directory
    project_path = target_dir / project_name

    if project_path.exists():
        print(f"Error: Directory '{project_path}' already exists.")
        sys.exit(1)

    # Create directory structure
    print(f"Creating project '{project_name}'...")

    project_path.mkdir(parents=True)
    (project_path / "assets").mkdir()
    (project_path / "scenes").mkdir()
    (project_path / "scripts").mkdir()

    # Create files
    files_to_create = [
        ("main.py", MAIN_PY_TEMPLATE),
        ("settings.py", SETTINGS_PY_TEMPLATE),
        ("requirements.txt", REQUIREMENTS_TXT_TEMPLATE),
        ("pyproject.toml", PYPROJECT_TEMPLATE),
        ("build.py", BUILD_PY_TEMPLATE),
        ("README.md", README_MD_TEMPLATE),
        (".gitignore", GITIGNORE_TEMPLATE),
        ("scripts/__init__.py", SCRIPTS_INIT_TEMPLATE),
        ("assets/.gitkeep", ""),
    ]

    for filename, template in files_to_create:
        filepath = project_path / filename
        filepath.parent.mkdir(parents=True, exist_ok=True)
        content = template.format(project_name=project_name)
        filepath.write_text(content, encoding="utf-8")
        print(f"  Created {filename}")

    # Make build.py executable on Unix
    if os.name != "nt":
        build_py = project_path / "build.py"
        build_py.chmod(build_py.stat().st_mode | 0o111)

    print(f"\n✓ Project '{project_name}' created successfully!")
    print(f"  Location: {project_path.absolute()}")
    print(f"\nNext steps:")
    print(f"  cd {project_name}")
    print(f"  pip install -r requirements.txt")
    print(f"  pyengine run        # Run the game")
    print(f"  pyengine build      # Build executable")

    return project_path


