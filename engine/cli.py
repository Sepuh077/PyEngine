def cmd_run(args) -> None:
    """Handle run command."""
    project_path = Path(args.project_path or ".").resolve()
    main_file = project_path / "main.py"

    if not main_file.exists():
        print(f"Error: No main.py found in {project_path}")
        print("Make sure you're in a valid Engine3D project directory.")
        sys.exit(1)

    print(f"Running project: {project_path.name}")

    try:
        # Run the main.py file
        result = subprocess.run(
            [sys.executable, str(main_file)] + args.args, cwd=project_path
        )
        sys.exit(result.returncode)
    except KeyboardInterrupt:
        print("\nGame stopped.")
        sys.exit(0)


