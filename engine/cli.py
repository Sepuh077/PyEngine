def _resolve_initial_scene(value):
    if isinstance(value, str) and value.strip().lower().endswith(".scene"):
        return Scene3D.load(value)

    if isinstance(value, str) and ":" in value:
        mod_name, cls_name = value.split(":", 1)
        module = __import__(mod_name, fromlist=[cls_name])
        cls = getattr(module, cls_name)
        return cls()

    if isinstance(value, type) and issubclass(value, Scene3D):
        return value()

    if isinstance(value, Scene3D):
        return value

    raise ValueError(
        "INITIAL_SCENE must be a .scene path string, a 'module:ClassName' string, "
        "a Scene3D subclass, or a Scene3D instance"
    )


