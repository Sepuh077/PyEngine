def main():
    """Main CLI entry point."""
    parser = argparse.ArgumentParser(
        prog="pyengine",
        description="Engine3D CLI - Create and manage 3D game projects",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  pyengine startproject mygame       Create a new project called 'mygame'
  pyengine run                       Run the current project
  pyengine build                     Build executable
  pyengine build --onefile           Build single executable file
  pyengine build --backend nuitka    Build with Nuitka
  pyengine editor                    Launch the editor
  pyengine --version                 Show version information
        """,
    )

    parser.add_argument("--version", action="version", version="%(prog)s 0.1.0")

    subparsers = parser.add_subparsers(dest="command", help="Available commands")

    # startproject command
    startproject_parser = subparsers.add_parser(
        "startproject",
        help="Create a new Engine3D project",
        description="Create a new Engine3D project with the given name.",
    )
    startproject_parser.add_argument("name", help="Name of the project to create")
    startproject_parser.add_argument(
        "-d",
        "--directory",
        help="Directory where project will be created (default: current directory)",
        default=None,
    )

    # build command
    build_parser = subparsers.add_parser(
        "build",
        help="Build the project executable",
        description="Build the game as a standalone executable.",
    )
    build_parser.add_argument(
        "--backend",
        choices=["pyinstaller", "nuitka"],
        default="pyinstaller",
        help="Build backend to use (default: pyinstaller)",
    )
    build_parser.add_argument(
        "--onefile", action="store_true", help="Build as single executable file"
    )
    build_parser.add_argument(
        "--debug", action="store_true", help="Build debug version with console window"
    )
    build_parser.add_argument("--clean", action="store_true", help="Clean build files")
    build_parser.add_argument(
        "--project-path",
        help="Path to project directory (default: current directory)",
        default=None,
    )

    # run command
    run_parser = subparsers.add_parser(
        "run",
        help="Run the project",
        description="Run the game from the project directory.",
    )
    run_parser.add_argument(
        "--project-path",
        help="Path to project directory (default: current directory)",
        default=None,
    )
    run_parser.add_argument(
        "args",
        nargs=argparse.REMAINDER,
        help="Additional arguments to pass to the game",
        default=[],
    )

    # editor command
    editor_parser = subparsers.add_parser(
        "editor",
        help="Launch the Engine3D editor",
        description="Launch the visual editor for the project.",
    )
    editor_parser.add_argument(
        "--project-path",
        help="Path to project directory (default: current directory)",
        default=None,
    )

    # Parse arguments
    args = parser.parse_args()

    if args.command == "startproject":
        create_project(args.name, args.directory)
    elif args.command == "build":
        cmd_build(args)
    elif args.command == "run":
        cmd_run(args)
    elif args.command == "editor":
        cmd_editor(args)
    else:
        parser.print_help()
        sys.exit(1)


if __name__ == "__main__":
    main()
